// Textures
//  Speech bubble
static GLuint tex_bubble;
static GLuint tex_speaker[NUM_SPEAKERS];

// display-lists
static GLuint list_bubble;
static GLuint list_speaker;
static GLuint list_font;

// current state
static char msg_line[3][80];
static int msg_speaker;

// helper function: draw a string anywhere
GLvoid glPrint(GLshort x, GLshort y, const char *text)
{
	glRasterPos2s(x,y);

	glPushAttrib(GL_LIST_BIT);			  // Pushes The Display List Bits	 ( NEW )
	glListBase(list_font - 32);				  // Sets The Base Character to 32	( NEW )
	glCallLists(strlen(text), GL_UNSIGNED_BYTE, text);  // Draws The Display List Text  ( NEW )
	glPopAttrib();					  // Pops The Display List Bits   ( NEW )
}

int message_init()
{
	char buffer[64];
	int i;

	tex_bubble = load_texture("img/hud/bubble.png",GL_NEAREST,GL_NEAREST);

	// Speaker icons
	for (i=0; i<NUM_SPEAKERS; i++)
	{
		sprintf(buffer,"img/hud/speakers/%d.png",i);
		tex_speaker[i] = load_texture(buffer,GL_LINEAR, GL_LINEAR);
	}

	// one for the speech bubble
	list_bubble = glGenLists(1);
	glNewList(list_bubble, GL_COMPILE);
		// ENable alpha test: yes to transparency for speech bubble / speaker icon
		glEnable(GL_ALPHA_TEST);
		glBindTexture(GL_TEXTURE_2D, tex_bubble);
		glBegin(GL_QUADS);
			glTexCoord2f(0,0);
			glVertex2i(144, SCREEN_Y - 32);
			glTexCoord2f(1,0);
			glVertex2i(SCREEN_X - 144, SCREEN_Y - 32);
			glTexCoord2f(1,1);
			glVertex2i(SCREEN_X - 144, SCREEN_Y);
			glTexCoord2f(0,1);
			glVertex2i(144, SCREEN_Y);
		glEnd();
	glEndList();

	// one for the current speaker (bind texture first...)
	list_speaker = glGenLists(1);
	glNewList(list_speaker, GL_COMPILE);
		glBegin(GL_QUADS);
			glTexCoord2f(0,0);
			glVertex2i(144, SCREEN_Y - 32);
			glTexCoord2f(1,0);
			glVertex2i(144 + 32, SCREEN_Y - 32);
			glTexCoord2f(1,1);
			glVertex2i(144 + 32, SCREEN_Y);
			glTexCoord2f(0,1);
			glVertex2i(144, SCREEN_Y);
		glEnd();
	glEndList();

	// Make one display list for each character.
	list_font = glGenLists(96);

	for (i=0; i < 96; i++)
	{
		glNewList(list_font + i, GL_COMPILE);
			glBitmap(8,8,0,0,8,0,bmp_font[i]);
		glEndList();
	}

	return EXIT_SUCCESS;
}

void message_clear()
{
	msg_speaker = -1;
	memset(msg_line,240,'\0');
}

void message_post(int speaker, const char *message)
{
	strcpy(msg_line[2],msg_line[1]);
	strcpy(msg_line[1],msg_line[0]);
	strcpy(msg_line[0],message);
	msg_speaker = speaker;
}

void message_draw()
{
	glCallList(list_bubble);
	// if there is a speaker...
	if (msg_speaker >= 0)
	{
		glBindTexture(GL_TEXTURE_2D, tex_speaker[msg_speaker]);
		glCallList(list_speaker);
	}

	glDisable(GL_ALPHA_TEST);
	glDisable(GL_TEXTURE_2D);

	// draw both lines.
	glColor3f(0,0,0);
	glPrint(200,SCREEN_Y-5,msg_line[0]);
	glPrint(200,SCREEN_Y-13,msg_line[1]);
	glPrint(200,SCREEN_Y-21,msg_line[2]);
	glColor3f(1,1,1);
	glEnable(GL_TEXTURE_2D);
}

void message_quit()
{
	glDeleteLists(list_font, 96);
	glDeleteLists(list_speaker, 1);
	glDeleteLists(list_bubble, 1);

	glDeleteTextures( NUM_SPEAKERS, tex_speaker );
	glDeleteTextures( 1, &tex_bubble );
}

